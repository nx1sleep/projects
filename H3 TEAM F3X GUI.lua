local function SJRTXB_script() 
	local script = Instance.new('LocalScript', TextButton)

	local function ChangeSkyOnClick()
		
		script.Parent.MouseButton1Click:Connect(function()
	
			local player = game.Players.LocalPlayer
			local tool
	
			for i, v in player:GetDescendants() do
				if v.Name == "SyncAPI" then
					tool = v.Parent
					break
				end
			end
	
			if not tool then
				for i, v in game.ReplicatedStorage:GetDescendants() do
					if v.Name == "SyncAPI" then
						tool = v.Parent
						break
					end
				end
			end
	
			if tool and tool:FindFirstChild("SyncAPI") then
				local remote = tool.SyncAPI.ServerEndpoint
	
				local function InvokeF3X(args)
					remote:InvokeServer(unpack(args))
				end
	
				local skyId = "117516137"
	
				local char = player.Character
				if char and char:FindFirstChild("HumanoidRootPart") then
					local pos = char.HumanoidRootPart.Position
					local x, y, z = math.floor(pos.X), math.floor(pos.Y), math.floor(pos.Z)
	
					local createArgs = {
						[1] = "CreatePart",
						[2] = "Normal", 
						[3] = CFrame.new(x, y + 10, z),
						[4] = workspace
					}
					InvokeF3X(createArgs)
	
					for i, v in workspace:GetDescendants() do
						if v:IsA("BasePart") and 
							math.floor(v.Position.X) == x and 
							math.floor(v.Position.Z) == z and
							v.Parent == workspace then
	
							local nameArgs = {
								[1] = "SetName",
								[2] = {[1] = v},
								[3] = "CustomSky"
							}
							InvokeF3X(nameArgs)
	
							local meshArgs = {
								[1] = "CreateMeshes", 
								[2] = {[1] = {["Part"] = v}}
							}
							InvokeF3X(meshArgs)
	
							local setMeshArgs = {
								[1] = "SyncMesh",
								[2] = {
									[1] = {
										["Part"] = v,
										["MeshId"] = "rbxassetid://8006679977" 
									}
								}
							}
							InvokeF3X(setMeshArgs)
	
							local textureArgs = {
								[1] = "SyncMesh", 
								[2] = {
									[1] = {
										["Part"] = v,
										["TextureId"] = "rbxassetid://" .. skyId
									}
								}
							}
							InvokeF3X(textureArgs)
	
							local resizeArgs = {
								[1] = "SyncMesh",
								[2] = {
									[1] = {
										["Part"] = v,
										["Scale"] = Vector3.new(500, 500, 500) 
									}
								}
							}
							InvokeF3X(resizeArgs)
	
							local lockArgs = {
								[1] = "SetLocked",
								[2] = {[1] = v},
								[3] = true
							}
							InvokeF3X(lockArgs)
	
							local collisionArgs = {
								[1] = "SyncCollision",
								[2] = {
									[1] = {
										["Part"] = v,
										["CanCollide"] = false
									}
								}
							}
							InvokeF3X(collisionArgs)
	
							local transArgs = {
								[1] = "SyncMaterial", 
								[2] = {
									[1] = {
										["Part"] = v,
										["Transparency"] = 0
									}
								}
							}
							InvokeF3X(transArgs)
	
							break
						end
					end
				else
					
				end
			else
				
			end
		end)
	end
	
	ChangeSkyOnClick()
end
