local function AKHGYM_script() 
	local script = Instance.new('LocalScript', TextButton_4)

	local button = script.Parent
	
	local function findSyncTool()
	    local player = game.Players.LocalPlayer
	    local tool
	    for i, v in player:GetDescendants() do
	        if v.Name == "SyncAPI" then
	            tool = v.Parent
	            break
	        end
	    end
	    if not tool then
	        for i, v in game.ReplicatedStorage:GetDescendants() do
	            if v.Name == "SyncAPI" then
	                tool = v.Parent
	                break
	            end
	        end
	    end
	    return tool
	end
	
	local function getSyncRemote(tool)
	    if tool and tool:FindFirstChild("SyncAPI") and tool.SyncAPI:FindFirstChild("ServerEndpoint") then
	        return tool.SyncAPI.ServerEndpoint
	    end
	    return nil
	end
	
	local function InvokeF3X(args, remote)
	    if remote then
	        remote:InvokeServer(unpack(args))
	    end
	end
	
	button.MouseButton1Click:Connect(function()
	    local tool = findSyncTool()
	    local remote = getSyncRemote(tool)
	    if not remote then return end
	
	    local Players = game:GetService("Players")
	    for i, plr in Players:GetPlayers() do
	        if plr.Character and plr.Character:FindFirstChild("HumanoidRootPart") then
	            local pos = plr.Character.HumanoidRootPart.Position
	            local x, y, z = math.floor(pos.X), math.floor(pos.Y), math.floor(pos.Z)
	            for j = 1, 5 do
	                local offset = 5 + (j-1)*2
	                local spawnCFrame = CFrame.new(x, y + offset, z)
	                local createArgs = {
	                    [1] = "CreatePart",
	                    [2] = "Normal",
	                    [3] = spawnCFrame,
	                    [4] = workspace
	                }
	                InvokeF3X(createArgs, remote)

	                local newPart = nil
	                for k, v in workspace:GetDescendants() do
	                    if v:IsA("BasePart") and v.Parent == workspace then
	                        if (v.Position - spawnCFrame.Position).Magnitude < 1 then
	                            newPart = v
	                            break
	                        end
	                    end
	                end
	
	                if newPart then
	                    local anchorArgs = {
	                        [1] = "SyncProperties",
	                        [2] = {
	                            [1] = {
	                                ["Part"] = newPart,
	                                ["Anchored"] = false
	                            }
	                        }
	                    }
	                    InvokeF3X(anchorArgs, remote)
	                end
	            end
	        end
	    end
	end)

end
