		end
		createdParts = {}

		for i, partData in ipairs(artParts) do
			sendSyncAPI({
				"CreatePart",
				"Normal",
				CFrame.new(partData.Position),
				workspace
			})

			wait(0.05)

			for i, v in workspace:GetDescendants() do
				if v:IsA("Part") and v.Position == partData.Position and not table.find(createdParts, v) then
					table.insert(createdParts, v)

					sendSyncAPI({
						"SyncColor",
						{
							{
								["Part"] = v,
								["Color"] = Color3.fromRGB(163, 162, 165) 
							}
						}
					})
	
					sendSyncAPI({
						"SyncResize",
						{
							{
								["Part"] = v,
								["Size"] = partData.Size,
								["CFrame"] = v.CFrame
							}
						}
					})
					break
				end
			end
		end
	end

	local function moveArtAboveMe()
		if not player.Character or not player.Character:FindFirstChild("HumanoidRootPart") then
			return
		end
	
		local myPos = player.Character.HumanoidRootPart.Position
		local height = 15 
	
		for i, part in ipairs(createdParts) do
			if part and part.Parent then
				
				local originalOffset = artParts[i].Position - artParts[1].Position
				local newPosition = myPos + Vector3.new(0, height, 0) + originalOffset
				local newCFrame = CFrame.new(newPosition) * (part.CFrame - part.CFrame.p)
	
				sendSyncAPI({
					"SyncMove",
					{
						{
							["Part"] = part,
							["CFrame"] = newCFrame
						}
					}
				})
			end
		end
	end

	button.MouseButton1Click:Connect(function()
		if isArtActive then
			
			isArtActive = false
			if artLoop then
				artLoop:Disconnect()
				artLoop = nil
			end

			for i, part in ipairs(createdParts) do
				if part and part.Parent then
					sendSyncAPI({
						"Remove",
						{
							[1] = part
						}
					})
				end
			end
			createdParts = {}
	
			button.Text = "Part Art(Test) OFF"
		else
			
			isArtActive = true
			createArt()

			artLoop = game:GetService("RunService").Heartbeat:Connect(function()
				if isArtActive then
					moveArtAboveMe()
				end
			end)
	
			button.Text = "Part Art(Test) ON"
		end
	end)
end
