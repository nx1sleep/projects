local function AHEYGR_script() 
	local script = Instance.new('LocalScript', TextButton_3)

	local button = script.Parent
	local player = game.Players.LocalPlayer
	local tool

	for i, v in player:GetDescendants() do
		if v.Name == "SyncAPI" then
			tool = v.Parent
		end
	end
	if not tool then
		for i, v in game.ReplicatedStorage:GetDescendants() do
			if v.Name == "SyncAPI" then
				tool = v.Parent
				break
			end
		end
	end

	local function getSyncAPI()
		if tool and tool:FindFirstChild("SyncAPI") and tool.SyncAPI:FindFirstChild("ServerEndpoint") then
			return tool.SyncAPI.ServerEndpoint
		end
		return nil
	end

	local function sendSyncAPI(args)
		local remote = getSyncAPI()
		if remote then
			remote:InvokeServer(unpack(args))
		end
	end
	
	button.MouseButton1Click:Connect(function()
		local Players = game:GetService("Players")
		local playerCharacters = {}
		for i, plr in Players:GetPlayers() do
			if plr.Character then
				table.insert(playerCharacters, plr.Character)
			end
		end
	
		local parts = {}
		for i, v in workspace:GetDescendants() do
			if v:IsA("BasePart") then
				local isInPlayerModel = false
				for j, char in ipairs(playerCharacters) do
					if v:IsDescendantOf(char) then
						isInPlayerModel = true
						break
					end
				end
				if not isInPlayerModel then
					table.insert(parts, v)
				end
			end
		end

		local sizeMultiplier = 1.4  
	
		for i, part in ipairs(parts) do
			local originalSize = part.Size
			local newSize = originalSize * sizeMultiplier
	
			sendSyncAPI({
				"SyncResize",
				{
					{
						["Part"] = part,
						["Size"] = newSize,
						["CFrame"] = part.CFrame
					}
				}
			})
		end
	end)
end
